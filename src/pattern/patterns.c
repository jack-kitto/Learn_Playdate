LCDPattern *Pattern_new(Pattern p) {
  switch (p) {
  case PATTERN_GREY12_5:
    return &grey12_5;
  case PATTERN_GREY25:
    return &grey25;
  case PATTERN_GREY37_5:
    return &grey37_5;
  case PATTERN_GREY50:
    return &grey50;
  case PATTERN_GREY62_5:
    return &grey62_5;
  case PATTERN_GREY75:
    return &grey75;
  case PATTERN_GREY87_5:
    return &grey87_5;
  case PATTERN_WHITE:
    return &white;
  case PATTERN_BLACK:
    return &black;
  default:
    return &black;
  }
};
